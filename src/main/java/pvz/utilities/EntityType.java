package pvz.utilities;

/**
 * Enumeration of all possible types of entities that can appear in the game.
 */
public enum EntityType {
    /** Represents a Peashooter plant. */
    PEASHOOTER,
    /** Represents a Sunflower plant. */
    SUNFLOWER,
    /** Represents a Wall-nut plant. */
    WALLNUT,
    /** Represents a Basic Zombie enemy. */
    BASICZOMBIE,
    /** Represents a Fast Zombie enemy. */
    FASTZOMBIE,
    /** Represents a Strong Zombie enemy. */
    STRONGZOMBIE,
    /** Represents a Beast Zombie enemy. */
    BEASTZOMBIE,
    /** Represents a bullet shot by a Peashooter. */
    BULLET,
    /** Represents a lawnmower. */
    LAWNMOWER
}
