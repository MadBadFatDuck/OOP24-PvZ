package pvz.model.zombies.api;

/**
 * Enum representing the four types of zombies in the game.
 */
public enum ZombieType {
    /** Basic zombie with standard attributes. */
    BASICZOMBIE,
    /** Fast zombie with increased speed. */
    FASTZOMBIE,
    /** Strong zombie with higher health and damage. */
    STRONGZOMBIE,
    /** Beast zombie with unique attributes. */
    BEASTZOMBIE
}